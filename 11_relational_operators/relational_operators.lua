--[[
    Lua provides the following relational operators
    < > <= >= == ~=

    All these operators always produce a boolean value.

    The == operator test for equality;
    the ~= operator is the negation of equality.
    We can apply these operators to any two values.
    If the values have different types, lua considers them not equial. Otherwise,
    luacompares the according to their types.

    Comparison of numbers always disregards their subtypes;
    it makes no difference whether the number is represented as an integer or as a float.
    Whats matter is its mathematical value.
]]

